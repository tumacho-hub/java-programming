package day_25_loops;

import javax.swing.*;

public class ForLoopFirst {
    public static void main(String[] args) {

        //  initialization ; condition; updates
        for (char i = 'z'; i >= 'a'; i--) {
            System.out.print(i + " ");
        }
        System.out.println("------------------------------------------------------------------------------");
        int name = 20;
        for (int i = name; name<=0; i++) {
            System.out.print(i+" ");
        }
        }
    }






