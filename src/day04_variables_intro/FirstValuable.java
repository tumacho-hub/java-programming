package day04_variables_intro;

public class FirstValuable {
    public static void main(String[] args) {
        //declare variable n that can store int(whole number)
        int n;
        //assign/put value 10 to n
        n = 10;
        //print value of n variable
        System.out.println(n);
        System.out.println("n");


        //declare variable apples, assign 50, print value out
        int apples;
        apples = 50;
        System.out.println(apples);

        //declare variable bananas, assign 100, print value out
        int bananas;
        bananas = 100;
        System.out.println(bananas);


    }
}


