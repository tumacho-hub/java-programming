package day_09S_scanner_practice;

public class RemainderOperrator {
    public static void main(String[] args) {
        System.out.println( 10 /3); //3
        System.out.println(10 % 3); //1
        System.out.println(10 / 5);//2
        System.out.println(10 % 5);//0 5+5+0
        System.out.println(20 /7);//2
        System.out.println(20 % 7);//7+7/6=20
        //** use . to get the exact amount! of the division when doing remainder
       //  exam: 100 cents -> 1
        // 115 cents -> 1
        //115 % 100 -> 100+15
        // 100 minutes -> 60minutes and 20 minutes







    }
}
