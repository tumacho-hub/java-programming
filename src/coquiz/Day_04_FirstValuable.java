package coquiz;

public class Day_04_FirstValuable {
    public static void main (String[] args) {
        //declare variable n that can store int(whole number)
        int n;
        //assign/put value 10 to n
        n = 10;
        //print value of n variable
        System.out.println(n);
        System.out.println("n"); // it is just n . not a contaner


        //declare variable apples, assign 50
        int apples; // declaration
        apples = 50; // assignment
        System.out.println(apples);

        //declare variable bananas, assign 100
        int bananas = 100; //declaration and assignment on same line
        System.out.println(bananas);

        //declaration + assignment in single statement:
        int bananass = 34;
        System.out.println(bananass);

        int students = 400;
        System.out.println(students);

        students = 401;
        System.out.println(students);

        int peopleInCity, birdsOnTree, fishInLake;
        peopleInCity = 1000;
        birdsOnTree = 21;
        fishInLake = 100;
        System.out.println(peopleInCity);
        System.out.println(birdsOnTree);
        System.out.println(fishInLake);


        int studentsInNY = 25, studentsInVA = 200;
        System.out.println(studentsInNY);
        System.out.println(studentsInVA);


    }
}
