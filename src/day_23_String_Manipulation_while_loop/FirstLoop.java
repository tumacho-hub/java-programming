package day_23_String_Manipulation_while_loop;

public class FirstLoop {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 5) {
            System.out.println("java is fun !");
            i++;

        }

        int apples = 5;

        while (apples <= 10) {
            System.out.println(apples + " apples are good");

            apples++;


        }
    }
}
