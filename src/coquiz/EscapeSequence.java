package coquiz;

public class EscapeSequence {
    public static void main(String[ ] args){
        // System.out.println("I like "java" programming"); will not work
        System.out.println("I like \"java\" programming"); // to put "" in print
        System.out.println(" lets buy \"coffe\" and \"tea\"");
        System.out.println("\\"); // to put \
        System.out.println("java\nintellij"); // to put next line
        System.out.println("\tjava\tselenium\tapi");// to tap make space

        System.out.println("\"iphone case\", \"iphone\", \"xbox\", \"webcam\", \"monitor\"");
        System.out.println();

        System.out.println("    ^    ");
        System.out.println("  /   \\");
        System.out.println("  |   | ");
        System.out.println("  _____");
        System.out.println();


        System.out.println("Company Name:\tGoogle\nEmployee:\t\tKhalid\nId:\t\t\t\t12345\nJob Tittle:\t\tSDET");
    }
}
